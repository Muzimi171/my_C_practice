
#include "array_stack.h"
// --- Private Structure Definition ---

// 这是栈的实际内部结构，对用户不可见。
// 你可以这么理解：这是栈的"内部实现细节"，用户只需要通过提供的接口函数来操作栈，而不需要关心它是如何实现的。
// 接口函数说白了就是我们提供给用户的"操作栈的工具"，也就是下面将要定义的那些函数。这些函数会使用这个内部结构来实现栈的功能，但用户不需要直接访问这个结构。
// 并且这样做也有助于封装和数据隐藏，使得代码更易维护和扩展， 防止用户直接访问和修改栈的内部数据，从而导致潜在的错误和不一致性。
struct Stack {

    void* data;          // 指向存储数据的连续内存块 (我们的"数组")

    size_t capacity;     // 栈的总容量（元素个数）

    size_t element_size; // 每个元素的大小（字节）

    int top;             // 栈顶索引，-1 表示空栈

};